# problem 9 - finding the area of a trapezoid

b1 = float(input("Enter the first base of a trapezoid: "))
b2 = float(input("Enter the second base of a trapezoid: "))
h = float(input("Enter the height of a trapezoid: "))

a = .5 * (b1 + b2) * h

print("The area of the trapezoid is", a, "square units")